void make_playermove(int i,int j)
{
    if(i>-1 && i<curr_size && j>-1 && j<curr_size) {


        String statedetails = getCorrectNumber(multi[i][j]) + getStateasString(color[i][j]);
        if (statedetails.equals("#")) {

            multi[i][j] = 1;
            color[i][j] = 1;
            green_state++;
        } else if (statedetails.equals("G")) {
            multi[i][j] = 2;
            color[i][j] = 1;
           // green_state++;
        } else if (statedetails.equals("2G")) {
            multi[i][j] = 3;
            color[i][j] = 1;
            //green_state++;
        } else if (statedetails.equals("3G")) {//
            multi[i][j]=0;
            color[i][j]=0;
            make_playermove(i, j + 1);
            make_playermove(i, j - 1);
            make_playermove(i + 1, j);
            make_playermove(i - 1, j);
            green_state--;
        } else if (statedetails.equals("R")) {
            multi[i][j] = 2;
            color[i][j] = 1;
            red_state--;
            green_state++;

        } else if (statedetails.equals("2R")) {
            multi[i][j] = 3;
            color[i][j] = 1;
            red_state--;
            green_state++;
        } else if (statedetails.equals("3R")) {
            multi[i][j] = 0;
            color[i][j] = 0;
            make_playermove(i, j + 1);
            make_playermove(i, j - 1);
            make_playermove(i + 1, j);
            make_playermove(i - 1, j);
            red_state--;
        }
    }
    else
    {
        return;
    }
}
